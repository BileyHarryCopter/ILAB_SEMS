char * strdupFRM (char *s) {
    char * p;
    // p = (char *) malloc( strlenFRM(s) + 1);

    p = calloc (strlenFRM (s), sizeof (char));

    if (p != NULL) { // NULL выводится в любом из случаев
        strcpyFRM (p, s);
    }

    return p;
}
