void strncatFRM (char *string, char *string1, int n) {

    while (*string != '\0') {
        string++;
    }

    char c = *(string1 + n);

    while (*string1 != c) {
        *string = *string1;
        string++;
        string1++;
    }

    *string = '\0';
}
