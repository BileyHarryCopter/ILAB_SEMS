//===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*//
            // вставляет содержимое первой строки во вторую строку
                  // без изменения содержимого второй строки
//===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*//
char * strcatFRM (char *string, const char *string1) {
    char * p = string;
    while (*string != '\0') {
        string++;
    }
    // *string = ' ';
    // string++;
    while (*string1 != '\0') {
        *string = *string1;
        string1++;
        string++;
    }
    *string = '\0';

    return p;
}
